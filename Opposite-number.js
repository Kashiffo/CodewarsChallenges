// DESCRIPTION:
// Very simple, given an integer or a floating-point number, find its opposite.

// Examples:

// 1: -1
// 14: -14
// -34: 34

let oppsiteNumber = (num1) => {
  if (num1 < 0) {
    console.log(-num1);
  } else if (num1 > 0) {
    console.log(-num1);
  }
};
oppsiteNumber(32);

// Solution {  const opposite = number => -number;
